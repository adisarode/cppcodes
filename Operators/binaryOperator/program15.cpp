//Relational operator
#include<iostream>

int main() {

	int x=5;
	int y=4;

	std::cout<<(x<=y)<<std::endl;      //0
	std::cout<<(x==y)<<std::endl;      //0
	std::cout<<(x>=y)<<std::endl;      //1
	std::cout<<(x!=y)<<std::endl;      //1
	std::cout<<(x<y)<<std::endl;       //0
	std::cout<<(x>y)<<std::endl;       //1

	return 0;

}
